public class ScoreInfo {


    /**
     * read : 3.5
     * vocabulary : 4
     * grammar : 4.5
     * writing : 4
     * hearing : 4.3
     * oracy : 4.25
     * score : 3
     */

    private float read;
    private float vocabulary;
    private float grammar;
    private float writing;
    private float hearing;
    private float oracy;
    private float score;

    public float getRead() {
        return read;
    }

    public void setRead(float read) {
        this.read = read;
    }

    public float getVocabulary() {
        return vocabulary;
    }

    public void setVocabulary(float vocabulary) {
        this.vocabulary = vocabulary;
    }

    public float getGrammar() {
        return grammar;
    }

    public void setGrammar(float grammar) {
        this.grammar = grammar;
    }

    public float getWriting() {
        return writing;
    }

    public void setWriting(float writing) {
        this.writing = writing;
    }

    public float getHearing() {
        return hearing;
    }

    public void setHearing(float hearing) {
        this.hearing = hearing;
    }

    public float getOracy() {
        return oracy;
    }

    public void setOracy(float oracy) {
        this.oracy = oracy;
    }

    public float getScore() {
        return score;
    }

    public void setScore(float score) {
        this.score = score;
    }
}
